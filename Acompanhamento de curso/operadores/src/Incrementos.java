public class Incrementos {
    public static void main(String[] args) {
        int numero = 5;

        //numero = numero + 1;

        //Apresenta o número e depois incrementa
        System.out.println(numero++);
        System.out.println(numero);

        //Incrementa o valor e depois apresenta
        System.out.println(++numero);
        System.out.println(numero);

        // ! | É o sinal da negação

        boolean variavel = true;

        variavel = !variavel;

        System.out.println(variavel);

    }

}
